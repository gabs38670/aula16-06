let num1 =Number(prompt('Digite o primeiro número'));
let num2 =Number(prompt('Digite o segundo número'));

// Templates string
console.log(`O valor de Nº 1 é ${num1}`);
console.log(`O valor de Nº 2 é ${num2}`);
// Concatenação
console.log("O valor de Nº 1 é" + num1);
console.log("O valor de Nº 2 é" + num2 );

//Soma
console.log (` A soma de ${num1} + ${num2} é igual a ${num1 + num2}`)

//Subtração
console.log (` A subtração de ${num1} - ${num2} é igual a ${num1 - num2}`)

//Multiplicação
console.log (` A multiplicação de ${num1} * ${num2} é igual a ${num1 * num2}`)

//Divisão
console.log (` A divisão de ${num1} / ${num2} é igual a ${num1 / num2}`)

//Resto da divisão
console.log (` o resto de ${num1} % ${num2} é igual a ${num1 % num2}`)
